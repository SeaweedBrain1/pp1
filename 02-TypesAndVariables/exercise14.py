#####
# Calculation of circle area and circumference 
##

radius = 5
pi = 3.14                                                                               # determine radius and PI
area = pi*(radius**2)                                                                   # calculate area 
circumference = 2*pi*radius                                                             # calculate circumference 
print(f'Area of the circle: {area}, circumference of the circle: {circumference}')      # display results